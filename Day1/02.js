//print the number using for loop
console.log("Using a for loop");

for (let i = 0; i <= 10; i++) {
  console.log(i);
}

console.log("Using a While loop");
let i = 0;
while (i <= 10) {
  console.log(i);
  i++;
}

console.log("Using a Do while loop");
let j = 1;
do {
  console.log(j);
  j++;
} while (j <= 8);
